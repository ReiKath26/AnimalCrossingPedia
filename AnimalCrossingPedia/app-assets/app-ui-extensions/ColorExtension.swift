import Foundation
import UIKit

extension UIColor
{
    static let softGreen: UIColor = UIColor(named: "softGreen") ?? UIColor.lightGray
    static let softYellow: UIColor = UIColor(named: "softYellow") ?? UIColor.lightGray
    static let softPink: UIColor = UIColor(named: "softPink") ?? UIColor.lightGray
    static let softBrown: UIColor = UIColor(named: "softBrown") ?? UIColor.lightGray
    
    static let accentGreen: UIColor = UIColor(named: "accentGreen") ?? UIColor.darkGray
    static let accentYellow: UIColor = UIColor(named: "accentYellow") ?? UIColor.darkGray
    static let accentPink: UIColor = UIColor(named: "accentPink") ?? UIColor.darkGray
    static let accentBrown: UIColor = UIColor(named: "accentBrown") ?? UIColor.darkGray
}
